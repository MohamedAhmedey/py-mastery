"""
Additon(+)
SUbtraction(-)
Multiplication(*)
Division(/)
Modulus(%)
Exponentiation(**)
Floor division(//)
"""
x= int(input("Enter your first value : "))
y= int(input("Enter your second value : "))

#Addition operator
print("The total is :", x+y)
#Subtraction operator
print("The difference is :", x-y)
#Multiplication operator
print("The product is :", x*y)
#Division operator
print("The quotient is :", x/y)
#Modulus operator
print("The modulus(remainder) is :", x%y)
#Exponentiation operator
print("The power is :", x**y)
#Floor division operator
print("The whole number(Integer) is :", x//y)
print(50%8)
print(50//8)
print(2**5)
