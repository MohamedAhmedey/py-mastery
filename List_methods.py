Fruits =["Oranges", "apples", "Bananas", "Pineapples", "Lemons", "Grapes", "Avocados", "Tomatoes"]
#Appending
Fruits.append("Kiwi")
print(Fruits)
#Clear
#Fruits.clear()
print(Fruits)
#Count enables you to know the number of duplicates in a list
x= Fruits.count("apples")
print(x)
#Insert
Fruits.insert(4,"Pawpaw")
print(Fruits)
#pop(Removes an item based on index)
Fruits.pop(4)
print(Fruits)
#Remove
Fruits.remove("Kiwi")
print(Fruits)
